pub fn key_to_string(key: &Key) -> Option<&'static str> {
    match key {
        Key::Alt => Some("alt_l"),
        Key::AltGr => Some("alt_r"),
        Key::Backspace => Some("backspace"),
        Key::CapsLock => Some("caps_lock"),
        Key::ControlLeft => Some("ctrl_l"),
        Key::ControlRight => Some("ctrl_r"),
        Key::Delete => Some("delete"),
        Key::DownArrow => Some("down"),
        Key::End => Some("end"),
        Key::Escape => Some("esc"),
        Key::F1 => Some("f1"),
        Key::F2 => Some("f2"),
        Key::F3 => Some("f3"),
        Key::F4 => Some("f4"),
        Key::F5 => Some("f5"),
        Key::F6 => Some("f6"),
        Key::F7 => Some("f7"),
        Key::F8 => Some("f8"),
        Key::F9 => Some("f9"),
        Key::F10 => Some("f10"),
        Key::F11 => Some("f11"),
        Key::F12 => Some("f12"),
        Key::Home => Some("home"),
        Key::Insert => Some("insert"),
        Key::LeftArrow => Some("left"),
        Key::NumLock => Some("numpad_lock"),
        Key::PageDown => Some("page_down"),
        Key::PageUp => Some("page_up"),
        Key::Pause => Some("pause"),
        Key::PrintScreen => Some("print_screen"),
        Key::Return => Some("enter"),
        Key::RightArrow => Some("right"),
        Key::ScrollLock => Some("scroll_lock"),
        Key::ShiftLeft => Some("shift_l"),
        Key::ShiftRight => Some("shift_r"),
        Key::Space => Some("space"),
        Key::Tab => Some("tab"),
        Key::UpArrow => Some("up"),
        Key::Unknown(vk) => vk_to_string(*vk as u64),
        _ => None,
    }
}

pub fn button_to_string(button: &Button) -> Option<&'static str> {
    match button {
        Button::Left => Some("mouse_left"),
        Button::Right => Some("mouse_right"),
        Button::Middle => Some("mouse_middle"),
        _ => None,
    }
}

